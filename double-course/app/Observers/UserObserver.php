     * Handle the User "created" event.
     *
     * @param  \App\Models\User  $user
     * @return void
     */
    public function created(User $user)
    {
        $code = $this->generateRandomString();

        while (User::where('code',$code)->get()->count()){
            $code=$this->generateRandomString();
        }

        $user->code = $code;
        $user->save();
    }

