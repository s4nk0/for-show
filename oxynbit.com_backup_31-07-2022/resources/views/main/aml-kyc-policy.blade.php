                                    <p><strong>Verification procedures</strong></p>

                                    <p>To help fight the funding of terrorism and money laundering activities, the Law requires all financial institutions to obtain, verify and record information that identifies each person or entity who opens an Account. We are required by Law to ask you to provide your name, address, date of birth and other information about you, your organisation or persons related to your organisation that will allow us to identify you before we approve your Account. We also may ask you to provide certain identifying documents, such as your driver&rsquo;s license or passport or your organisation&rsquo;s articles of incorporation and may obtain credit and other consumer reports to assist us in verifying your identity and in determining whether you satisfy our account criteria. Unless you provide the requested information and documentation, we may not be able to open your Account.</p>

                                    <p>By applying for an Bitrouse Account, you agree to provide the information and documentation requested by Bitrouse and consent to Bitrouse&rsquo;s acquisition of credit and other consumer reports about you for the purposes described above.</p>

                                    <p>The Bitrouse&rsquo;s Customer due diligence (&ldquo;<strong>CDD</strong>&ldquo;) procedure requires the Users to provide Bitrouse with reliable, independent source documents to identify them and confirm their residential address. Such documents may include, for example, a national ID card, an international passport, a bank statement, a utility bill. For such purposes, Bitrouse reserves the right to collect the Users&rsquo; identification information for the purposes of compliance with its AML/CFT and KYC Policy.</p>

                                    <p>Bitrouse will take steps to confirm the authenticity of documents and information provided by Users. Bitrouse will also look to verify identification information through secondary sources, and Bitrouse reserves the right to investigate further the cases of Users where Bitrouse is not satisfied with the documentation submitted.</p>

